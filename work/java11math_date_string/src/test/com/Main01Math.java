package test.com;

public class Main01Math {
    public static void main(String[] args) {
        System.out.println("hello");

        System.out.println(Math.PI); //원주율
        System.out.println(Math.abs(-200)); //절대값
        System.out.println(Math.random()); //임의의 난수
        System.out.println(Math.ceil(4.34)); //올림
        System.out.println(Math.floor(4.99)); //버림
        System.out.println(Math.round(4.5)); //반올림
        System.out.println(Math.pow(2,8)); //제곱
        System.out.println(Math.sqrt(256)); //제곱근

    } //end main
} //end class
